    am5.ready(function () {

      // Create root element
      var root = am5.Root.new("categoryChart");

      // Set themes
      root.setThemes([
        am5themes_Animated.new(root)
      ]);

      // Create chart
      var chart = root.container.children.push(
        am5percent.PieChart.new(root, {
          layout: root.verticalLayout,
          endAngle: 270
        })
      );

      // Create series
      var series = chart.series.push(
        am5percent.PieSeries.new(root, {
          valueField: "value",
          categoryField: "category",
          endAngle: 270
        })
      );

      series.states.create("hidden", {
        endAngle: -90
      });

      // Set data
      series.data.setAll([{
        category: "Mens",
        value: 15
      }, {
        category: "Womens",
        value: 25
      }, {
        category: "Kids",
        value: 35
      }, {
        category: "Home & Furniture",
        value: 25
      }]);

      // Create legend
      var legend = chart.children.push(
        am5.Legend.new(root, {
          centerX: am5.percent(50),
          x: am5.percent(50),
          marginTop: 15,
          marginBottom: 15,
        })
      );

      legend.data.setAll(series.dataItems);
      series.appear(1000, 100);

    }); // end am5.ready()
  </script>
